#include <stdio.h>

int main() {
    int num, square;
    
    // Reading an integer
    printf("Enter an integer: ");
    scanf("%d", &num);
    
    // Calculating the square
    square = num * num;
    
    // Displaying the square
    printf("The square of %d is: %d\n", num, square);
    
    return 0;
}
