#include <stdio.h>

int main() {
    int a, b, sum;
    
    // Reading two integers
    printf("Enter two integers: ");
    scanf("%d %d", &a, &b);
    
    // Calculating the sum
    sum = a + b;
    
    // Displaying the sum
    printf("The sum of %d and %d is: %d\n", a, b, sum);
    
    return 0;
}
